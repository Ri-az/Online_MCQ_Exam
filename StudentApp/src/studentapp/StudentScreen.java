    private void startActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startActionPerformed
        // TODO add your handling code here:
        //port number, user name, ip address collect & send them to the server to connect properly
        ipa=ip.getText();
        portn=port.getText();
        uname=username.getText();
        if(ipa.isEmpty() || portn.isEmpty() || uname.isEmpty()){
            JOptionPane.showMessageDialog(this,"write all the information.");
        }
        else{  
            try {
                soc=new Socket(ipa,Integer.parseInt(portn));
                pw=new PrintWriter(new OutputStreamWriter(soc.getOutputStream()));
                pw.println(uname);
                pw.println();
                pw.flush();
                Thread t=new Thread(this);
                t.start();
                start.setEnabled(false);
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, "Can't send properly.");
            }
        }
    }//GEN-LAST:event_startActionPerformed

    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        //answer check & create result
        bsub=1;
        question.setText("");
        for(int i=1;i<=pos;i++){
            if(stuans[i].equals(ans[i]))count1++;
            //System.out.println(ans[i]+"   "+stuans[i]+"\n"); ///to check the result
        }
        
        try {
            PrintWriter pw1=new PrintWriter(new OutputStreamWriter(soc.getOutputStream()));
            pw1.println("result "+uname+" "+count1);
            pw1.println();
            pw1.flush();
            start.setEnabled(true);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Can't send result properly.");
        }
        
        JOptionPane.showMessageDialog(this, "Your earned marks is: "+count1,"Result",PLAIN_MESSAGE,null);
        
    }//GEN-LAST:event_submitActionPerformed

    
