    @Override
    public void run() {
        //collect question & answer & time and show them to the proper place
        String qs="";
        try {
            BufferedReader br=new BufferedReader(new InputStreamReader(soc.getInputStream()));
            while(true){
                String s;
                s=br.readLine();
                if(s.equals("End"))break;
                qs+=s+"\n";
                
            }
            while(true){
                String sp;
                sp=br.readLine();
                if(sp.equals("Endanswer"))break;
                ans[pos]=sp;
                pos++;
            }
            String sp1;
            sp1=br.readLine();
            end_hour=sp1;
            sp1=br.readLine();
            end_minit=sp1;
            //System.out.println(end_hour+" "+end_minit);
            
            hour=Integer.parseInt(end_hour);
            minit=Integer.parseInt(end_minit);
            count=0;
            
            JOptionPane.showMessageDialog(this, "Are you ready to start?");
            question.append(qs+"\n");
            //time work
            t = new Timer(1000, new ActionListener(){
                
                @Override
                public void actionPerformed(ActionEvent e) {
                    clock.setText(hour+" : "+minit+" : "+count);
                    if(bsub==1)t.stop();
                    else if(hour==0 && minit==0){
                        if(count==0){
                            t.stop();
                            question.setText("");
                            submit.setEnabled(false);
                            for(int i=1;i<=pos;i++){
                                if(stuans[i].equals(ans[i]))count1++;
                            }
                            try {
                                PrintWriter pw1=new PrintWriter(new OutputStreamWriter(soc.getOutputStream()));
                                pw1.println("result "+uname+" "+count1);
                                pw1.println();
                                pw1.flush();
                                start.setEnabled(true);
                            } catch (IOException ex) {
                                JOptionPane.showMessageDialog(null, "Can't send result properly.");
                            }
                            JOptionPane.showMessageDialog(null, "Your earned marks is: "+count1,"Result",PLAIN_MESSAGE,null);
                        }
                    }
                    
                    else if(count==0 && minit==0){
                        hour--;
                        minit=59;
                        count=60;
                    }
                    else if(count==0 || minit==-1){
                        if(minit==-1){
                            hour--;
                            minit=59;
                        }
                        else{
                            minit--;
                            count=60;
                        }
                    }
                    count--;
                }
            });
            t.start();
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this,"Can't read Question.");
        }
    }
